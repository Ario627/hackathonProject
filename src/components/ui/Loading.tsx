'use client'

interface LoadingProps {
  size?: 'sm' | 'md' | 'lg'
  text?: string
  fullScreen?: boolean
}

export function Loading({ size = 'md', text, fullScreen = false }: LoadingProps) {
  const sizes = {
    sm: 'h-6 w-6',
    md: 'h-10 w-10',
    lg: 'h-16 w-16',
  }

  const spinner = (
    <div className="flex flex-col items-center gap-3">
      <svg
        className={`animate-spin text-blue-600 ${sizes[size]}`}
        xmlns="http://www. w3.org/2000/svg"
        fill="none"
        viewBox="0 0 24 24"
      >
        <circle
          className="opacity-25"
          cx="12"
          cy="12"
          r="10"
          stroke="currentColor"
          strokeWidth="4"
        />
        <path
          className="opacity-75"
          fill="currentColor"
          d="M4 12a8 8 0 018-8V0C5. 373 0 0 5.373 0 12h4zm2 5. 291A7.962 7.962 0 014 12H0c0 3.042 1.135 5. 824 3 7.938l3-2.647z"
        />
      </svg>
      {text && <p className="text-gray-600 text-sm">{text}</p>}
    </div>
  )

  if (fullScreen) {
    return (
      <div className="fixed inset-0 z-50 flex items-center justify-center bg-white/80 backdrop-blur-sm">
        {spinner}
      </div>
    )
  }

  return spinner
}

// Skeleton loader
interface SkeletonProps {
  className?: string
  variant?: 'text' | 'circular' | 'rectangular'
  width?: string | number
  height?: string | number
}

export function Skeleton({
  className = '',
  variant = 'text',
  width,
  height,
}: SkeletonProps) {
  const variants = {
    text: 'rounded',
    circular: 'rounded-full',
    rectangular: 'rounded-lg',
  }

  return (
    <div
      className={`animate-pulse bg-gray-200 ${variants[variant]} ${className}`}
      style={{
        width: width,
        height: height || (variant === 'text' ? '1em' : undefined),
      }}
    />
  )
}

// Page loading skeleton
export function PageSkeleton() {
  return (
    <div className="space-y-6 p-6">
      <Skeleton variant="rectangular" height={40} className="w-1/3" />
      <div className="space-y-3">
        <Skeleton variant="text" className="w-full" height={20} />
        <Skeleton variant="text" className="w-4/5" height={20} />
        <Skeleton variant="text" className="w-3/5" height={20} />
      </div>
      <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
        <Skeleton variant="rectangular" height={150} />
        <Skeleton variant="rectangular" height={150} />
        <Skeleton variant="rectangular" height={150} />
      </div>
    </div>
  )
}